void main() {
  //Contoh operator aritmatika di dart

  // Penambahan
  int a = 5;
  int b = 3;
  int hasilpenambahan = a + b;
  print("Hasil Penambahan: $hasilpenambahan");

  // Pengurangan
  int c = 10;
  int d = 4;
  int hasilpengurangan = c - d;
  print("Hasil Pengurangan: $hasilpengurangan");

  // Perkalian
  int e = 3;
  int f = 6;
  int hasilperkalian = e * f;
  print("Hasil Perkalian: $hasilperkalian");

  // Pembagian
  double g = 15;
  double h = 2;
  double hasilpembagian = g / h;
  print("Hasil Pembagian: $hasilpembagian");

  // Modulus ( sisa pembagian )
  int i = 15;
  int j = 4;
  int sisaPembagian = i % j;
  print("Sisa pembagian: $sisaPembagian");
}
